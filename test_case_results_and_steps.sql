-- Table for test case results
CREATE TABLE test_case_results (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    story_id VARCHAR2(100),
    summary VARCHAR2(4000),
    description VARCHAR2(4000),
        prompt CLOB,

    generated_test_case CLOB
);

-- Sequence for test_case_results id
CREATE SEQUENCE test_case_results_seq START WITH 1 INCREMENT BY 1;

-- Table for test steps (linked to test_case_results)
CREATE TABLE test_case_steps (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    test_case_result_id NUMBER REFERENCES test_case_results(id),
    step_number NUMBER,
    step_description VARCHAR2(4000),
    expected_output VARCHAR2(4000)
);
